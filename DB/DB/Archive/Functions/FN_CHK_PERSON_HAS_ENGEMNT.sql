CREATE FUNCTION FN_CHK_PERSON_HAS_ENGEMNT(
	AV_PERSON_ID VARCHAR(45)
) RETURNS varchar(6) 
    DETERMINISTIC
BEGIN
	DECLARE LI_SFI_FLAG INT DEFAULT 0;
	
					SELECT COUNT(*) INTO LI_SFI_FLAG
					FROM PERSON_ENTITY
					WHERE PERSON_ID = AV_PERSON_ID
					AND VERSION_STATUS = 'ACTIVE';
			
					IF LI_SFI_FLAG > 0 THEN
						RETURN 'TRUE';
						ELSE 
						RETURN 'FALSE';
					END IF;
END;

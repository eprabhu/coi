CREATE FUNCTION `FN_IS_TRAVEL_DESTINATION_ADDED`(
    TRAVEL_DISCLOSURE_ID INT
) RETURNS tinyint(1)
    READS SQL DATA
    DETERMINISTIC
BEGIN
    IF EXISTS (SELECT 1
            FROM COI_TRAVEL_DESTINATIONS CTD
            WHERE CTD.TRAVEL_DISCLOSURE_ID = TRAVEL_DISCLOSURE_ID
        ) THEN
        RETURN TRUE;
    END IF;

    RETURN FALSE;
end

CREATE PROCEDURE `GET_COI_TRAVEL_DISCLOSURE_ADMIN_DASHBOARD`(
		AV_DISCLOSURE_PERSON_ID         VARCHAR(40),
		AV_END_DATE                     VARCHAR(10),
		AV_ENTITY_ID                  	VARCHAR(90),
		AV_CERTIFICATION_DATE           VARCHAR(10),
		AV_HOME_UNIT                    VARCHAR(8),
		AV_TRAVEL_STATUS_CODE       	VARCHAR(20),
		AV_DOCUMENT_STATUS_CODE			VARCHAR(20),
		AV_REVIEW_STATUS_CODE			VARCHAR(20),
		AV_COUNTRY_CODE					VARCHAR(20),
		AV_ADMINISTRATOR                VARCHAR(200),
        AV_SORT_TYPE                    VARCHAR(500),
        AV_PAGED                        INT,
        AV_LIMIT                        INT,
        AV_UNLIMITED                    BOOLEAN,
		AV_TYPE                         VARCHAR(1),
		AV_IS_COUNT                    	BOOLEAN,
        AV_TRIP_TITLE                   VARCHAR(200),
        AV_TRAVEL_START_DATE            DATETIME,
        AV_TRAVEL_END_DATE              DATETIME,
		AV_PERSON_ID                    VARCHAR(40)
		
)
BEGIN
DECLARE LS_DYN_SQL LONGTEXT;
DECLARE LS_FILTER_CONDITION LONGTEXT;
DECLARE LS_OFFSET_CONDITION VARCHAR(600);
DECLARE LS_OFFSET INT;
DECLARE TAB_QUERY LONGTEXT;
DECLARE JOIN_CONDITION LONGTEXT;
DECLARE SELECTED_FIELD_LIST LONGTEXT;
DECLARE LS_ADMIN_GROUP_FCOI_CONDITION LONGTEXT;
DECLARE LS_ADMIN_GROUP_PROJECT_CONDITION LONGTEXT;
DECLARE LI_LATE_SUBMISSION_DUE_DAYS INT;

SET LS_OFFSET = (AV_LIMIT * AV_PAGED);
SET LS_FILTER_CONDITION ='';
SET LS_DYN_SQL ='';
SET JOIN_CONDITION = '';
SET SELECTED_FIELD_LIST= '';
SET TAB_QUERY = '';

IF AV_TYPE ='A' THEN

				IF AV_DISCLOSURE_PERSON_ID IS NOT NULL AND AV_DISCLOSURE_PERSON_ID <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.PERSON_ID = ''',AV_DISCLOSURE_PERSON_ID,''' AND ');

				END IF;

				 IF AV_END_DATE IS NOT NULL AND AV_END_DATE <> '' THEN

					 SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' DATE(T.EXPIRATION_DATE) = ''', STR_TO_DATE(AV_END_DATE,'%Y-%m-%d'),''' AND ');
				  
				 END IF;

				IF AV_CERTIFICATION_DATE IS NOT NULL AND AV_CERTIFICATION_DATE <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' DATE(T.CERTIFIED_AT) = ''', STR_TO_DATE(AV_CERTIFICATION_DATE,'%Y-%m-%d'),''' AND ');
				  
				END IF;
				 
				IF AV_ENTITY_ID IS NOT NULL  AND AV_ENTITY_ID <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.ENTITY_ID =',AV_ENTITY_ID, '  AND ');

				END IF;

				IF AV_HOME_UNIT IS NOT NULL AND AV_HOME_UNIT <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.UNIT = ''',AV_HOME_UNIT,''' AND ');

				END IF;

				/* IF AV_TRAVEL_STATUS_CODE IS NOT NULL  AND AV_TRAVEL_STATUS_CODE <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.TRAVEL_STATUS_CODE IN (',AV_TRAVEL_STATUS_CODE,') AND ');

				END IF; */

				IF AV_DOCUMENT_STATUS_CODE IS NOT NULL  AND AV_DOCUMENT_STATUS_CODE <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.DOCUMENT_STATUS_CODE IN (',AV_DOCUMENT_STATUS_CODE,') AND ');

				END IF;

				IF AV_REVIEW_STATUS_CODE IS NOT NULL  AND AV_REVIEW_STATUS_CODE <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.REVIEW_STATUS_CODE IN (',AV_REVIEW_STATUS_CODE,') AND ');

				END IF;

				IF AV_ADMINISTRATOR IS NOT NULL AND AV_ADMINISTRATOR <> '' THEN
			
					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T.ADMIN_PERSON_ID IN (''', REPLACE(AV_ADMINISTRATOR, ',', ''','''),''') AND ');

				END IF;

				IF AV_COUNTRY_CODE IS NOT NULL AND AV_COUNTRY_CODE <> '' THEN

					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION, ' FIND_IN_SET(''', AV_COUNTRY_CODE, ''', T.COUNTRY_CODES) > 0 AND ');

				END IF;

				IF AV_TRIP_TITLE IS NOT NULL AND AV_TRIP_TITLE <> '' THEN
					IF INSTR(AV_TRIP_TITLE, '*') > 0 THEN
						SET AV_TRIP_TITLE = REPLACE(AV_TRIP_TITLE, '*', '%');
					ELSE
						SET AV_TRIP_TITLE = CONCAT('%', AV_TRIP_TITLE, '%');
					END IF;

					SET LS_FILTER_CONDITION = CONCAT(
						LS_FILTER_CONDITION,
						' T.TRIP_TITLE LIKE ''', AV_TRIP_TITLE, ''' AND '
					);
				END IF;

				IF AV_TRAVEL_START_DATE IS NOT NULL AND AV_TRAVEL_END_DATE IS NOT NULL THEN
				IF AV_TRAVEL_START_DATE <= AV_TRAVEL_END_DATE THEN
					SET LS_FILTER_CONDITION = CONCAT(
						LS_FILTER_CONDITION,
						' (T.TRAVEL_START_DATE <= ''', AV_TRAVEL_END_DATE, ''' AND T.TRAVEL_END_DATE >= ''', AV_TRAVEL_START_DATE, ''') AND '
					);
				ELSE
					SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION, ' 1=0 AND ');
				END IF;
				ELSEIF AV_TRAVEL_START_DATE IS NOT NULL THEN
					SET LS_FILTER_CONDITION = CONCAT(
						LS_FILTER_CONDITION,
						' T.TRAVEL_END_DATE >= ''', AV_TRAVEL_START_DATE, ''' AND '
						);
				ELSEIF AV_TRAVEL_END_DATE IS NOT NULL THEN
					SET LS_FILTER_CONDITION = CONCAT(
						LS_FILTER_CONDITION,
						' T.TRAVEL_START_DATE <= ''', AV_TRAVEL_END_DATE, ''' AND '
						);
				END IF;

ELSE 

SET TAB_QUERY = CONCAT(TAB_QUERY,'WHERE T1.REVIEW_STATUS_CODE IN (2,3,4,5) AND T1.VERSION_STATUS IN (''PENDING'',''ACTIVE'') ');

END IF;

IF AV_IS_COUNT = FALSE THEN
	IF AV_SORT_TYPE IS NULL OR AV_SORT_TYPE = '' THEN
			SET AV_SORT_TYPE =  CONCAT(' ORDER BY T.UPDATE_TIMESTAMP DESC ');
	ELSE
		SET AV_SORT_TYPE = CONCAT(' ORDER BY ',AV_SORT_TYPE);
	END IF;
ELSE
	SET AV_SORT_TYPE = "";
END IF;

IF AV_UNLIMITED = TRUE OR AV_IS_COUNT = TRUE THEN
        SET LS_OFFSET_CONDITION = '';
ELSE
        SET LS_OFFSET_CONDITION = CONCAT(' LIMIT ',AV_LIMIT,' OFFSET ',LS_OFFSET);
END IF;

IF LS_FILTER_CONDITION <>'' THEN
        SET LS_FILTER_CONDITION = CONCAT(' WHERE ',LS_FILTER_CONDITION);
        SELECT TRIM(TRAILING 'AND ' FROM LS_FILTER_CONDITION) into LS_FILTER_CONDITION from dual;
END IF;

    SELECT VALUE INTO LI_LATE_SUBMISSION_DUE_DAYS  FROM PARAMETER WHERE PARAMETER_NAME = 'TRAVEL_DISCL_SUBMISSION_DUE_DAYS';

	SET SELECTED_FIELD_LIST= CONCAT(SELECTED_FIELD_LIST,'T1.TRAVEL_DISCLOSURE_ID,
							T5.UNIT_NUMBER AS UNIT,
							T5.UNIT_NAME,
                            T4.REVIEW_STATUS_CODE,
                            T4.DESCRIPTION AS REVIEW_STATUS_DESCRIPTION,
                            T9.RELATIONSHIP_TYPES AS TRAVELER_TYPE_DESCRIPTION,
							T2.PRIMARY_NAME AS TRAVEL_ENTITY_NAME,
							T1.ENTITY_ID,
							T3.FULL_NAME AS TRAVELLER_NAME,
                            T45.VALUE AS TRAVEL_AMOUNT,
                            T1.EXPIRATION_DATE,
                            T41.VALUE AS PURPOSE_OF_THE_TRIP,
                            T43.VALUE AS TRIP_TITLE,
                            T1.UPDATE_TIMESTAMP,
                            T1.VERSION_STATUS,
                            T1.CREATE_TIMESTAMP,
							T10.DOCUMENT_STATUS_CODE,
							T10.DESCRIPTION AS DOCUMENT_STATUS_DESCRIPTION,
							CASE
                                WHEN TIMESTAMPDIFF(DAY, T1.CREATE_TIMESTAMP, T1.CERTIFIED_AT) > 30 THEN true
                                    ELSE false
                            END AS IS_LATE_SUBMISSION,
							T1.CERTIFIED_AT,
							T1.ADMIN_PERSON_ID,
							T1.ADMIN_GROUP_ID,
							T1.PERSON_ID,
							T1.TRAVEL_NUMBER,
							T46.DESCRIPTION AS FUNDING_TYPE,
							T47.FULL_NAME AS UPDATE_USER_FULL_NAME,
                            T49.MIN_START_DATE AS TRAVEL_START_DATE,
							T49.MAX_END_DATE AS TRAVEL_END_DATE,
                            T50.TRAVEL_DESTINATIONS,
                            T51.FULL_NAME AS ADMIN_PERSON_NAME,
							T52.ADMIN_GROUP_NAME,
							T48.COUNTRY_CODES,
                            FN_DISCLOSURE_TOTAL_COMMENT_COUNT(T1.TRAVEL_DISCLOSURE_ID, ''', AV_PERSON_ID, ''', 24) AS DISCLOSURE_COMMENT_COUNT');

	SET JOIN_CONDITION = CONCAT(' LEFT JOIN ENTITY T2 ON T2.ENTITY_ID=T1.ENTITY_ID
							LEFT JOIN COI_TRAVEL_REVIEW_STATUS T4 ON T4.REVIEW_STATUS_CODE=T1.REVIEW_STATUS_CODE
                             LEFT JOIN UNIT T5 ON T5.UNIT_NUMBER = T1.HOME_UNIT
							LEFT JOIN(SELECT T21.TRAVEL_DISCLOSURE_ID, GROUP_CONCAT(DISTINCT T25.DESCRIPTION ORDER BY T25.VALID_PERS_ENTITY_REL_TYP_CODE ASC) AS RELATIONSHIP_TYPES FROM COI_TRAVEL_DISCLOSURE T21
								LEFT JOIN COI_TRAVEL_DISCLOSURE_TRAVELER T27 ON T27.TRAVEL_DISCLOSURE_ID = T21.TRAVEL_DISCLOSURE_ID
								LEFT JOIN VALID_PERSON_ENTITY_REL_TYPE T25 ON T25.VALID_PERS_ENTITY_REL_TYP_CODE = T27.VALID_PERS_ENTITY_REL_TYP_CODE
								GROUP BY T21.TRAVEL_DISCLOSURE_ID)T9 ON T9.TRAVEL_DISCLOSURE_ID = T1.TRAVEL_DISCLOSURE_ID
							LEFT JOIN COI_TRAVEL_DOCUMENT_STATUS_TYPE T10 ON T10.DOCUMENT_STATUS_CODE = T1.DOCUMENT_STATUS_CODE
                            LEFT JOIN FB_COMP_CUSTOM_ELEMENT T40 ON T40.CUSTOM_ELEMENT_NAME = "Purpose"
							LEFT JOIN FB_COMP_CUSTOM_ELEMENT_ANSWER T41 ON T41.CUSTOM_DATA_ELEMENTS_ID = T40.CUSTOM_DATA_ELEMENTS_ID AND T41.MODULE_ITEM_CODE = 24 AND T41.MODULE_ITEM_KEY = T1.TRAVEL_DISCLOSURE_ID
							LEFT JOIN FB_COMP_CUSTOM_ELEMENT T42 ON T42.CUSTOM_ELEMENT_NAME = "Trip Title"
							LEFT JOIN FB_COMP_CUSTOM_ELEMENT_ANSWER T43 ON T43.CUSTOM_DATA_ELEMENTS_ID = T42.CUSTOM_DATA_ELEMENTS_ID AND T43.MODULE_ITEM_CODE = 24 AND T43.MODULE_ITEM_KEY = T1.TRAVEL_DISCLOSURE_ID
							LEFT JOIN FB_COMP_CUSTOM_ELEMENT T44 ON T44.CUSTOM_ELEMENT_NAME = "Reimbursed Cost"
							LEFT JOIN FB_COMP_CUSTOM_ELEMENT_ANSWER T45 ON T45.CUSTOM_DATA_ELEMENTS_ID = T44.CUSTOM_DATA_ELEMENTS_ID AND T45.MODULE_ITEM_CODE = 24 AND T45.MODULE_ITEM_KEY = T1.TRAVEL_DISCLOSURE_ID
							LEFT JOIN COI_TRAVEL_FUNDING_TYPE T46 ON T46.FUNDING_TYPE_CODE = T1.FUNDING_TYPE_CODE
							LEFT JOIN (SELECT TRAVEL_DISCLOSURE_ID, GROUP_CONCAT(DISTINCT COUNTRY_CODE ORDER BY COUNTRY_CODE SEPARATOR '','') AS COUNTRY_CODES FROM COI_TRAVEL_DESTINATIONS GROUP BY TRAVEL_DISCLOSURE_ID) T48 ON T48.TRAVEL_DISCLOSURE_ID = T1.TRAVEL_DISCLOSURE_ID
							LEFT JOIN (SELECT TD.TRAVEL_DISCLOSURE_ID, GROUP_CONCAT(C.COUNTRY_NAME SEPARATOR ''||'') AS TRAVEL_DESTINATIONS FROM COI_TRAVEL_DESTINATIONS TD LEFT JOIN COUNTRY C ON C.COUNTRY_CODE = TD.COUNTRY_CODE GROUP BY TD.TRAVEL_DISCLOSURE_ID) T50 ON T50.TRAVEL_DISCLOSURE_ID = T1.TRAVEL_DISCLOSURE_ID
                           LEFT JOIN (SELECT TRAVEL_DISCLOSURE_ID, MIN(STAY_START_DATE) AS MIN_START_DATE, MAX(STAY_END_DATE) AS MAX_END_DATE FROM COI_TRAVEL_DESTINATIONS GROUP BY TRAVEL_DISCLOSURE_ID) T49 ON T49.TRAVEL_DISCLOSURE_ID = T1.TRAVEL_DISCLOSURE_ID
                            LEFT JOIN PERSON T51 ON T51.PERSON_ID = T1.ADMIN_PERSON_ID
							LEFT JOIN ADMIN_GROUP T52 ON T52.ADMIN_GROUP_ID = T1.ADMIN_GROUP_ID
							LEFT JOIN PERSON T3 ON T3.PERSON_ID=T1.PERSON_ID 
							LEFT JOIN PERSON T47 ON T47.PERSON_ID=T1.UPDATED_BY ');
                        
	IF AV_IS_COUNT = TRUE THEN
		SET LS_DYN_SQL = CONCAT('SELECT  DISTINCT COUNT(*)  FROM ');

	ELSE
		SET LS_DYN_SQL = CONCAT('SELECT  DISTINCT *  FROM ');
	
	END IF;
     SET LS_DYN_SQL = CONCAT(LS_DYN_SQL,'(SELECT DISTINCT ',SELECTED_FIELD_LIST,'
							FROM COI_TRAVEL_DISCLOSURE T1 ', JOIN_CONDITION, TAB_QUERY,
							' )T ',LS_FILTER_CONDITION,' ',AV_SORT_TYPE,' ',LS_OFFSET_CONDITION);

SET @QUERY_STATEMENT = LS_DYN_SQL;
PREPARE EXECUTABLE_STATEMENT FROM @QUERY_STATEMENT;
EXECUTE EXECUTABLE_STATEMENT;

END
